# Re-form the word
# A word has been split into a left part and right part.
# Re-form the word by adding both halves together changing the first to an
# uppercase letter.
# input ---------> two strings from the user
# constraint ----> no
# output --------> concatenated string with caps in first character

def word_reformat():
    n1 =input()
    n2 =input()
    print(n1+n2.title())

